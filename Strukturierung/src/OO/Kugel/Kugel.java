package OO.Kugel;

public class Kugel {
    private int nr;
    private String matrial;
    private double durchmesser;
    private double oberflaeche;
    private double voloumen;
    private double gewicht;
    private int x;
    private int y;
    private int z;

    public int getNr() {
        return nr;
    }

    public void setNr(int nr) {
        this.nr = nr;
    }

    public String getMatrial() {
        return matrial;
    }

    public void setMatrial(String matrial) {
        this.matrial = matrial;
    }

    public double getDurchmesser() {
        return durchmesser;
    }

    public void setDurchmesser(double durchmesser) {
        this.durchmesser = durchmesser;
    }

    public double getOberflaeche() {
        return oberflaeche;
    }

    public void setOberflaeche(double oberflaeche) {
        this.oberflaeche = oberflaeche;
    }

    public double getVoloumen() {
        return voloumen;
    }

    public void setVoloumen(double voloumen) {
        this.voloumen = voloumen;
    }

    public double getGewicht() {
        return gewicht;
    }

    public void setGewicht(double gewicht) {
        this.gewicht = gewicht;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }
}
